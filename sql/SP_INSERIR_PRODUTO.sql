CREATE PROCEDURE SP_INSERIR_PRODUTO
(
	@NOME VARCHAR(255),
	@SLUG VARCHAR(255),
	@DESCRICAO VARCHAR(500),
	@PRECO NUMERIC(15,4),
	@ESTOQUE NUMERIC(15,4),
	@ATIVO BIT,
	@USUARIO_INCLUSAO INT
)
AS
BEGIN

INSERT INTO PRODUTOS
(
    NOME,
    SLUG,
    DESCRICAO,
    PRECO,
    ESTOQUE,
    ATIVO,
    USUARIO_INCLUSAO    
)
VALUES
(
    @NOME,
    @SLUG,
    @DESCRICAO,
    @PRECO,
    @ESTOQUE,
    @ATIVO,
    @USUARIO_INCLUSAO
)

END